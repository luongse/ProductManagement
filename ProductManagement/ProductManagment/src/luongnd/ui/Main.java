    private void tblViewSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewSupplierMouseClicked
        // TODO add your handling code here:
        try {
            //Neu click vao table thì là false để thực hiện cập nhật
            addNewSupplier = false;
            int row = tblViewSupplier.getSelectedRow();
            String id = (String) tblViewSupplier.getValueAt(row, 0);
            SupplierDAO dao = new SupplierDAO();
            SuppliersDTO dto = dao.findbyprimarykey(id);
            txtCodeSupllier.setText(dto.getSupCode());
            txtCodeSupllier.setEditable(false);
            txtNameSupllier.setText(dto.getSupName());
            txtAddressSupllier.setText(dto.getAddress());
            if (dto.isCollaborating()) {
                cbCollaborating.setSelected(true);
            } else {
                cbCollaborating.setSelected(false);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblViewSupplierMouseClicked

