    public void displayItem() throws SQLException, Exception {
        ArrayList<ItemDTO> list = new ItemDAO().getItem();
        tblModelUserItem.setRowCount(0);
        for (ItemDTO itemDTO : list) {
            tblModelUserItem.addRow(itemDTO.toVector());
        }

    }

    public void loadDaTaComBox() {
 
        try {
            cbSupplier.removeAllItems();
            SupplierDAO dao = new SupplierDAO();
            List<SuppliersDTO> listSupplier = dao.getSupplier();
            for (SuppliersDTO supplierDTO : listSupplier) {
                cbSupplier.addItem(supplierDTO.getSupCode() + "-" + supplierDTO.getSupName());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

