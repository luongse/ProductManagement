    private void tblViewItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewItemMouseClicked
        try {
            // TODO add your handling code here:
            addNewItem = false;
            int row = tblViewItem.getSelectedRow();
            String id = (String) tblViewItem.getValueAt(row, 0);
            ItemDAO dao = new ItemDAO();
            ItemDTO dto = dao.findbyprimarykey(id);
            txtItemCode.setText(dto.getItemCode());
            txtItemCode.setEditable(false);
            txtItemName.setText(dto.getItemName());
            txtUnit.setText(dto.getUnit());
            txtPrice.setText(String.valueOf(dto.getPrice()));
            cbSupplier.setSelectedItem(tblViewItem.getValueAt(row, 5).toString());
            if (dto.isSupplying()) {
                cbSupplying.setSelected(true);
            } else {
                cbSupplying.setSelected(false);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_tblViewItemMouseClicked

