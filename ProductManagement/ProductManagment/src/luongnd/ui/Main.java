    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int row = tblViewSupplier.getSelectedRow();
        String code = txtCodeSupllier.getText();
        if (code.length() > 10) {
            JOptionPane.showMessageDialog(this, "Smaller than 10 character");
            txtCodeSupllier.setText("");
            return;
        }

        String name = txtNameSupllier.getText();
        String address = txtAddressSupllier.getText();
        boolean collaborating = cbCollaborating.isSelected();
        String valid = "";
        SupplierDAO dao = new SupplierDAO();
        SuppliersDTO dto = new SuppliersDTO(code, name, address, collaborating);
        try {

            if (dao.checkBlank(code)) {
                valid += "Id can not be blank\n";
            }
            if (dao.checkBlank(name)) {
                valid += "Name can not be blank\n";
            }
            if (dao.checkBlank(address)) {
                valid += "Address can not be blank\n";
            }
            if (valid.equals("")) {

                if (addNewSupplier) {
                    ArrayList<SuppliersDTO> list = new SupplierDAO().getSupplier();
                    if (dao.checkDuplicate(list, code)) {
                        JOptionPane.showMessageDialog(this, "Duplicate");
                        return;
                    }
                    if (dao.insert(dto)) {
                        JOptionPane.showMessageDialog(this, "Insert Successful");
                        tblModelUserSupplier.addRow(dto.toVector());
                        cbSupplier.removeAllItems();
                        loadDaTaComBox();
                    }
                } else {
                    if (dao.update(dto)) {
                        JOptionPane.showMessageDialog(this, "Update successful");
                        tblModelUserSupplier.setValueAt(name, row, 1);
                        tblModelUserSupplier.setValueAt(address, row, 2);
                        cbSupplier.removeAllItems();
                        loadDaTaComBox();
                        displayItem();
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, valid);
            }

        } catch (Exception e) {
            //  e.printStackTrace();
//            if(e.getMessage().contains("duplicate")){
//                JOptionPane.showMessageDialog(this, "Duplicate");
//        }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int r = JOptionPane.showConfirmDialog(this, "Are you sure", "remove?", JOptionPane.YES_OPTION);
        if (r == JOptionPane.YES_OPTION) {
            try {
                int selectedRow = tblViewSupplier.getSelectedRow();
                String supCode = tblModelUserSupplier.getValueAt(selectedRow, 0).toString();
                ItemDAO itemDao = new ItemDAO();
                if (itemDao.CheckItems(supCode)) {
                    JOptionPane.showMessageDialog(this, "remove items of this supply first");
                    return;
                }
                SupplierDAO dao = new SupplierDAO();
                if (dao.delete(supCode)) {
                    JOptionPane.showMessageDialog(this, "Delete successful");
                    tblModelUserSupplier.removeRow(selectedRow);
                    btnAddNewActionPerformed(evt);
                    cbSupplier.removeAllItems();
                    loadDaTaComBox();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

